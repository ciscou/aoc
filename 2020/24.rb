DIR_TO_DELTA_POS = {
  "se" => [ 1,  1],
  "e"  => [ 0,  2],
  "ne" => [-1,  1],
  "sw" => [ 1, -1],
  "w"  => [ 0, -2],
  "nw" => [-1, -1]
}

grid = {}

dir_re = Regexp.new("#{DIR_TO_DELTA_POS.keys.map { |k| "(#{k})" }.join("|")}")

input.lines.each do |line|
  line.chomp!

  x = 0
  y = 0

  line.scan(dir_re).map(&:compact).map(&:first).each do |dir|
    dpos = DIR_TO_DELTA_POS[dir]
    dx, dy = dpos
    x += dx
    y += dy
  end

  grid[[x, y]] = !grid[[x, y]]
end

puts grid.values.count(&:itself)

def neightbours(c)
  x, y = c

  DIR_TO_DELTA_POS.values.map do |dpos|
    dx, dy = dpos

    [x + dx, y + dy]
  end
end

def evolve(grid)
  next_grid = {}

  neights = grid.keys.flat_map { |c| neightbours(c) }
  cells = grid.keys + neights
  cells.sort!.uniq!

  cells.each do |cell|
    x, y = cell

    ns = neightbours(cell).count { |c| grid[c] }

    next_grid[[x, y]] = case grid[[x, y]]
                        when true # black
                          if ns == 0
                            false
                          elsif ns > 2
                            false
                          else
                            true
                          end
                        else
                          if ns == 2
                            true
                          else
                            false
                          end
                        end
  end

  next_grid
end

puts grid.values.count(&:itself)
100.times do
  grid = evolve(grid)
  puts grid.values.count(&:itself)
end
