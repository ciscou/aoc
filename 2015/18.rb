def lights_at(grid, row, col)
  return 0 if row < 0
  return 0 if col < 0
  return 0 if row > grid.length - 1
  return 0 if col > grid.first.length - 1

  grid[row][col] ? 1 : 0
end

def count_adj_lights(grid, row, col)
  lights_at(grid, row+1, col+1) +
  lights_at(grid, row+1, col-1) +
  lights_at(grid, row-1, col+1) +
  lights_at(grid, row-1, col-1)+
  lights_at(grid, row-1, col) +
  lights_at(grid, row+1, col) +
  lights_at(grid, row, col-1) +
  lights_at(grid, row, col+1)
end

def lights_on(grid)
  sum = 0

  grid.each do |row|
    row.each do |col|
      sum += 1 if col
    end
  end

  sum
end

