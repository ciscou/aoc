      new_grid[row][col] = grid[row][col]

      if grid[row][col]
        new_grid[row][col] = false unless [2, 3].include? count_adj_lights(grid, row, col)
      else
        new_grid[row][col] = true if [3].include? count_adj_lights(grid, row, col)
      end
    end
  end

